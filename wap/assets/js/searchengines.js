const content = document.getElementById("content");

function renderGlossary(glossary) {
	content.innerHTML = "";
	glossary.forEach(item => {
		const div = document.createElement("div");
		div.className = "term";
		const h3 = document.createElement("h3");
		h3.innerText = item.term;
		const p = document.createElement("p");
		p.innerText = item.definition;
		if (item.link) {
			const a = document.createElement("a");
			a.href = item.link;
			a.textContent = "了解更多";
			a.className = "learn-more";
			a.target = "_blank"; // 在新页面中打开链接
			p.appendChild(document.createTextNode(" "));
			p.appendChild(a);
			div.addEventListener('click', function () {
				window.open(item.link);
			});
		}

		div.appendChild(h3);
		div.appendChild(p);
		content.appendChild(div);
	});
}

function filterGlossary(query) {
    return glossary.filter(item => {
        return item.term.toLowerCase().includes(query.toLowerCase()) || item.definition.toLowerCase().includes(query.toLowerCase());
    });
}


const searchInput = document.getElementById('search-input');
searchInput.addEventListener('input', updateGlossary);

function updateGlossary() {
	const query = searchInput.value;

	const filteredGlossary = filterGlossary(query);
	renderGlossary(filteredGlossary);
}


